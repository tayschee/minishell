#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	if (!s)
		return (0);
	while (n)
	{
		if (*(char *)s == c)
			return ((char *)s);
		s++;
		n--;
	}
	return (0);
}
